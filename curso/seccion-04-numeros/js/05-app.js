//----Incrementos o Decrementos----

let puntaje = 5;

//incremento

//Post-incremento
puntaje++;

//Pre-incremento
++puntaje;

puntaje = 2;

//decremento
--puntaje;

console.log(puntaje);


//otra forma de incremento o decremento
let puntaje2 = 5;
console.log(puntaje2);

puntaje2 += 3;
console.log(puntaje2);