//----Arrow Functions----

const aprendiendo = function () {
    console.log("Aprendiendo JavaScript");
}

//arrow function, si es de una sola linea no es necesaria las llaves y el return
const aprendiendo2 = () => console.log("Aprendiendo JavaScript");

//funcion flecha sin llaves y sin return
const aprendiendo3 = () => "Aprendiendo JavaScript";

aprendiendo();
aprendiendo2();
console.log(aprendiendo3());

